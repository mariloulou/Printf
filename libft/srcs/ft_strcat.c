char	*ft_strcat(char *s1, const char *s2)
{
	int buff1;
	int buff2;

	buff1 = 0;
	while (s1[buff1] != '\0')
	{
		buff1++;
	}
	buff2 = 0;
	while (s2[buff2] != '\0')
	{
		s1[buff1] = s2[buff2];
		buff1++;
		buff2++;
	}
	s1[buff1] = '\0';
	return (s1);
}
